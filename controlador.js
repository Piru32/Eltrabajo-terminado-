productos.forEach(function(producto){
    let foto = document.createElement("img")
    foto.classList.add("w-100", "img-fluid", "mt-2")
    foto.src = producto.foto
    
    let columna = document.createElement("div")
    columna.classList.add("col")

    let tarjeta = document.createElement("div")
    tarjeta.classList.add("card")

    let nombreProducto = document.createElement("h4")
    nombreProducto.classList.add("text-center")
    nombreProducto.textContent = producto.nombre

    let precioProducto = document.createElement("p")
    precioProducto.classList.add("card-text", "text-primary")
    precioProducto.textContent = "$" + producto.precio

    let estadoProducto = document.createElement("p")
    estadoProducto.classList.add("card-text", "text-success")
    estadoProducto.textContent = producto.estado 
    
    let popuProducto = document.createElement("p")
    popuProducto.classList.add("text-danger")
    popuProducto.textContent = "Popularidad: " + producto.popularidad

    //tarjeta.appenChild
    tarjeta.appendChild(foto)
    tarjeta.appendChild(nombreProducto)
    tarjeta.appendChild(precioProducto)
    tarjeta.appendChild(estadoProducto)
    tarjeta.appendChild(popuProducto)

    columna.appendChild(tarjeta)
    contenedor.appendChild(columna)

    //Condicion =true
    if(producto.estado == true){
        estadoProducto.classList.add("text-secondary")
        estadoProducto.textContent = "Estado: Nuevo"
    }

    else{
        estadoProducto.classList.add("text-secondary")
        estadoProducto.textContent = "Estado: Usado"
    }

})

